public class NumeroPositivoNegativo {
    public static void main(String[] args) {
        int numero = 32;
        if (numero > 0) {
            System.out.println(numero + " é positivo");
        } else if (numero < 0) {
            System.out.println(numero + " é negativo");
        } else {
            System.out.println(numero + " é zero");
        }
    }
}
